def sentence(phrase): # variable name defined
    phraseLen = len(phrase)# length of the phrase 
    if phraseLen == 1: # lenght of phrase is equal to 1
        return phrase # return the output of the phrase
    return [phrase[-1]] + sentence(phrase[:-1]) # reverses the order of the phrase

phraseList = ["This","is","awesome"] # defined list to reverse

print(sentence(phraseList))# prints the fianl outcome

















































































































